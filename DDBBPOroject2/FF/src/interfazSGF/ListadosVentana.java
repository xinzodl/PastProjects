    public ListadosVentana(int tipo, int nFilas, String [] listado,java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();                  
        
        javax.swing.JLabel [] labelTitulos=null;
         if(tipo<BufferDeRegistro.nombre_catalogos.length && tipo>0){
            labelTitulos=new javax.swing.JLabel [BufferDeRegistro.campos_catalogos[tipo].length];
            String titulo = BufferDeRegistro.nombre_catalogos[tipo];
            setTitle(titulo);
            String [] campos=BufferDeRegistro.campos_catalogos[tipo];
            for(int i=0;i<campos.length;i++){
                labelTitulos[i]=new javax.swing.JLabel(campos[i]+"\t\t");
            }
        }else{
            setTitle("Acceso invertido");
            labelTitulos = new javax.swing.JLabel[]{};
            jPTitulos.setVisible(false);
        }
        //Creamos la tabla:
         jPTitulos.setLayout(new java.awt.GridLayout(1, labelTitulos.length));          
         
         //Metemos tï¿½tulos de la tabla:
         for(int i=0;i<labelTitulos.length;i++){
            labelTitulos[i].setFont(new java.awt.Font("Tahoma", 3, 11));
            javax.swing.JPanel panel =new javax.swing.JPanel();
            panel.setLayout(new java.awt.FlowLayout(java.awt.FlowLayout.RIGHT));
            (panel).add(labelTitulos[i]);             
            (jPTitulos).add(labelTitulos[i]);            
         }
        //La rellenamos
        for (int i = 0; i < listado.length;i++){
             javax.swing.JTextArea jtarea = new javax.swing.JTextArea(listado[i]);             
             javax.swing.JPanel panel =new javax.swing.JPanel();
             panel.setLayout(new java.awt.FlowLayout(java.awt.FlowLayout.CENTER));                          
             if(i!=0)jTPListado.setText(jTPListado.getText()+"\n"+listado[i]);
             else jTPListado.setText(listado[i]);
        }
        jLFilas.setText((new Integer(nFilas)).toString()); 
        getContentPane().add(jSPListado);
        pack();
        repaint();
    }
    
